    public static int getRandomNumbers(int[] arreglo){
        //seleccionar numero random del arreglo
            Random random = new Random();
            int indexRandom = random.nextInt(arreglo.length);
            int numeroRandom = arreglo[indexRandom];
            
            //Operaci√≥n aleatoria
            int operacion = random.nextInt(4);
            
            int modificador = random.nextInt(5) + 1;
            
            switch(operacion){
                case 0:
                    numeroRandom += modificador;
                    break;
                case 1:
                    numeroRandom -= modificador;
                    break;
                case 2:
                    numeroRandom *= modificador;
                    break;
                case 3:
                    numeroRandom /= modificador;
                    break;
            }
            if(numeroRandom < arreglo[0]){
                numeroRandom = numeroRandom;
            } else if(numeroRandom > arreglo[0]){
                numeroRandom = arreglo[arreglo[indexRandom + 1]];
            }
            return numeroRandom;
        }
